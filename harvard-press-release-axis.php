                <h2 style="text-align: left; font-weight: 600;text-transform: capitalize;">Harvard Business School builds a case study on Axis My India</h2>
                <p style="text-align: justify;">Axis My India, is the first consumer data intelligence firm from the country to have become a part of the Harvard Business School curriculum. The Ivy League business school has built a case study based on its election forecasting model.</p>
                <p style="text-align: justify;">The case study, now a part of HBS’s classroom course on elections, highlights the complexity associated with successfully predicting elections in the world’s largest democracy, its diversity evident to all, with varied geographies, shared borders with six nations, sprawling rural populations and 23 different languages spoken across the country.</p>
                <p style="text-align: justify;">This case study written by Professor Ananth Raman, Senior Associate Ann Winslow and Research Associate Kairavi Dey discusses the process which goes behind forecasting elections in India and the methods AMI uses to reach such accurate forecasts – selection of field surveyors, hiring & training, use of technology for data collection, quality auditing, data analysis & final forecasting.</p>
